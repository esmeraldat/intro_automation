	public void hacerTortaMilanesa() {
		hacerPan();
		hacerMilanesa();
		servirTorta();

	}

	
	private void hacerPan() {
		sembrarTrigo();
		cidarTrigo();
		cosecharTrigo();
		hacerHarina();
		hacerMasa();
		hornearPan();
	}

	

	private void hacerMilanesa() {
		criarCerdo();
		sacrificarCerdo();
		prepararMilanesa();
	}


	private void servirTorta() {
		calentarPan();
		cortarMilanesa();
		embarrarCremaPan();
		agregarJitomate();
		agregarLechuga();
		agregarChile();
		agregarCebolla();
		conseguirPlato();
	}
